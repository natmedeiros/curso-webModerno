const a = 7
let b = 3

b += a // b = b + a, ou seja, o valor de "a" será somado com o valor de "b".
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2 (atribuição modular, se o resultado for par ele vai imprimir 0, se for ímpar ele vai imprimir 1)
console.log(b)